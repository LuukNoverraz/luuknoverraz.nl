// Get the current date
const currentDate = new Date();

// Determine the year value based on the current date and condition
let yearValue = 2; // Default value

if (currentDate > new Date('2024-09-30')) {
    yearValue = 3;
}

if (currentDate > new Date('2025-09-30')) {
    yearValue = 4;
}

function translatePage(language) {
    if (language == "nl") {
        nl.style.color = 'var(--red)';
        en.style.color = 'var(--black)';
    }
    if (language == "en") {
        nl.style.color = 'var(--black)';
        en.style.color = 'var(--red)';
    }

    let suffix;

    if (language === "nl") {
        suffix = "e";
    } else if (language === "en") {
        if (yearValue === 1) {
            suffix = "st";
        } else if (yearValue === 2) {
            suffix = "nd";
        } else if (yearValue === 3) {
            suffix = "rd";
        } else {
            suffix = "th";
        }
    }

    // Update the content of the span with the calculated year value and suffix
    document.getElementById('studyYear').textContent = `${yearValue}${suffix}`;
    
    const elements = document.querySelectorAll('[data-translate]');
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[language] && translations[language][key]) {
            element.textContent = translations[language][key];
        }
    });
}

